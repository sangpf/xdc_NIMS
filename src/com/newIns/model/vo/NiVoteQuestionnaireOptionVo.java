package com.newIns.model.vo;

/**
 * 该类仅供封装投票问卷选项 描述,选项图封装
 * @author 11409
 *
 */
public class NiVoteQuestionnaireOptionVo {
	
	private String optionDes;
	private String optionPic;
	private String optionFlag;
	
	public String getOptionDes() {
		return optionDes;
	}
	
	
	public void setOptionDes(String optionDes) {
		this.optionDes = optionDes;
	}
	public String getOptionPic() {
		return optionPic;
	}
	public void setOptionPic(String optionPic) {
		this.optionPic = optionPic;
	}

	public String getOptionFlag() {
		return optionFlag;
	}

	public void setOptionFlag(String optionFlag) {
		this.optionFlag = optionFlag;
	}
	
	
	
}
